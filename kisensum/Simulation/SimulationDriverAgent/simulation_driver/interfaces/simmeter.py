import logging
from simulation import SimulationRegister, SimulationInterface

_log = logging.getLogger(__name__)


class SimulationMeterRegister(SimulationRegister):
    """Simulated Meter Register."""

    attribute_list = ['power_kw',
                      'last_timestamp']


class Interface(SimulationInterface):
    """
        Simulated Meter Interface.

        Model the behavior of a meter, for instance a meter that takes power readings
        for a circuit. Publish the power readings in response to get_point requests.

        This simulated device interface is currently a placeholder.
        In order to make it useful, logic needs to be added here that defines what
        the power readings would be, most likely as a function of (simulated) time.
    """

    registers_from_config = []

    def update(self):
        """Update the device driver's state in advance of a periodic scrape request."""
        super(Interface, self).update()
        power_kw = None
        if power_kw:
            self.set_register_by_name('power_kw', power_kw)
